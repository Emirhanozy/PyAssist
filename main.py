class PyAssist:
    def __init__(self):
        # PYTHON_TUTORIALS sözlüğünü ve modül tanımlamalarını başlat
        self.tutorials = PYTHON_TUTORIALS
        self.current_topic = None
        self.modules = {
            "modül1": "Python programlamaya giriş için temel konular",
            "modül2": "Python'da daha ileri seviye konular ve uygulamalar"
        }

    def list_topics(self):
        # Tüm mevcut konuları ve detaylarını listele
        print("\nMevcut Konular:")
        for key, tutorial in self.tutorials.items():
            print(f"\n{tutorial['title']}:")
            print(f"Modül: {tutorial['difficulty']}")
            print(f"Alt Başlıklar: {', '.join(tutorial['topics'])}")
            print(f"Açıklama: {tutorial['description']}")
    
    def get_topic_by_keyword(self, keyword):
        # Anahtar kelimeye göre konu arama
        matching_topics = []
        for key, tutorial in self.tutorials.items():
            # Başlık, açıklama veya alt başlıklarda arama yap
            if (keyword.lower() in tutorial['title'].lower() or
                keyword.lower() in tutorial['description'].lower() or
                any(keyword.lower() in topic.lower() for topic in tutorial['topics'])):
                matching_topics.append(tutorial)
        return matching_topics

    def show_videos_for_topic(self, topic_key):
        # Seçilen konu için videoları göster
        if topic_key in self.tutorials:
            topic = self.tutorials[topic_key]
            print(f"\n{topic['title']} için mevcut videolar:")
            for video in topic['videos']:
                print(f"\n{video['order']}. {video['title']}")
                print(f"Konu: {video['topic']}")
                print(f"Video Linki: {video['url']}")
        else:
            print("\nBu konu için video bulunamadı.")

    def filter_by_module(self, module):
        # Belirli bir modüle ait konuları filtrele
        return {k: v for k, v in self.tutorials.items() 
                if v['difficulty'] == module}

def show_main_menu():
    # Ana menüyü göster ve kullanıcı seçimini al
    print("\n=== Python Eğitim Asistanı ===")
    print("1. Tüm Konuları Listele")
    print("2. Konu Ara")
    print("3. Modüle Göre Filtrele")
    print("4. Konu Videoları Göster")
    print("5. Çıkış")
    return input("Seçiminiz (1-5): ")

def main():
    # Ana uygulama döngüsü
    app = PyAssist()
    
    print("Python Eğitim Asistanına Hoş Geldiniz!")
    
    while True:
        # Kullanıcı seçimini al ve işle
        choice = show_main_menu()
        
        if choice == "1":
            # Tüm konuları listele
            app.list_topics()

        elif choice == "2":
            # Anahtar kelimeye göre konu ara
            keyword = input("\nAramak istediğiniz konu: ")
            matching_topics = app.get_topic_by_keyword(keyword)
            
            if matching_topics:
                print("\nEşleşen Konular:")
                for topic in matching_topics:
                    print(f"\n{topic['title']}")
                    print(f"Modül: {topic['difficulty']}")
                    print(f"Alt Başlıklar: {', '.join(topic['topics'])}")
                    print(f"Açıklama: {topic['description']}")
            else:
                print("\nAranan konuda eğitim bulunamadı.")

        elif choice == "3":
            # Modüle göre konuları filtrele
            print("\nMevcut Modüller: modül1, modül2")
            module = input("Modül seçin: ")
            filtered_topics = app.filter_by_module(module)
            
            if filtered_topics:
                print(f"\n{module.capitalize()} Konuları:")
                for key, topic in filtered_topics.items():
                    print(f"\n{topic['title']}")
                    print(f"Alt Başlıklar: {', '.join(topic['topics'])}")
                    print(f"Açıklama: {topic['description']}")
            else:
                print(f"\n{module} içinde konu bulunamadı.")

        elif choice == "4":
            # Seçilen konunun videolarını göster
            print("\nMevcut Konular:")
            for key in app.tutorials.keys():
                print(f"- {key}")
            topic_key = input("\nHangi konunun videolarını görmek istersiniz?: ")
            app.show_videos_for_topic(topic_key)

        elif choice == "5":
            # Programdan çık
            print("\nGüle güle!")
            break
            
        else:
            # Geçersiz seçim durumu
            print("\nGeçersiz seçim!")

# Programı başlat
if __name__ == "__main__":
    main()